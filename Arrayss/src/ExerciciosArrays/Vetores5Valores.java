package ExerciciosArrays;
import java.util.Scanner;

     //1-Faça um programa que crie um vetor por leitura com 5 valores de pontuação de uma atividade e o escreva em seguida. Encontre após a maior pontuação e a apresente

         
   //Resolução1

public class Vetores5Valores {
	
	
		public static void main(String[] args) {

			int maior = 0;
			Scanner ler = new Scanner(System.in);

			int [] array = new int[5];
			for(int i = 0; i < 5;i++) {
				System.out.println("Digite uma nota :");
				array[i] = ler.nextInt();

				if(array[i] > maior) {
					maior = array[i];
				}

			}
			for(int i = 0; i < 5;i++) {
				System.out.println((i+1) + "º : " + array[i]);
			}


			System.out.println("A maior Pontuação adquerido é " + maior);



		}
	}


